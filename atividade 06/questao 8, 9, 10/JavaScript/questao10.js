let num1, num2;

num1 = Number(prompt("insira o primeiro numero"));
num2 = Number(prompt("insira o segundo numero"));

document.writeln(`sua somatação dos numero ${num1} e ${num2} é ${num1 + num2} <br>`)
document.writeln(`sua subtração dos numero ${num1} e ${num2} é ${num1 - num2}<br>`)
document.writeln(`sua divisão dos numero ${num1} e ${num2} é ${num1 / num2}<br>`)
document.writeln(`sua multiplicaçao dos numero ${num1} e ${num2} é ${num1 * num2}<br>`)

